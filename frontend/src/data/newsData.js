// ฟังก์ชันช่วยในการดึงข่าวตาม ID (รองรับทั้งตัวเลขและรหัส 24 หลัก)
export const getNewsById = (id) => {
  return allNews.find(news => 
    news.id.toString() === id.toString() || 
    news._id === id
  );
};

// ฟังก์ชันดึงข่าวยอดนิยม
export const getPopularNews = () => {
  return allNews.filter(news => [5, 6, 7, 8].includes(news.id));
};

// ฟังก์ชันดึงข่าวสำหรับ Slider
export const getSliderNews = () => {
  return allNews.filter(news => [9, 10].includes(news.id));
};

// ฟังก์ชันดึงข่าวสำหรับ Grid
export const getGridNews = () => {
  return allNews.filter(news => [1, 2, 3, 4].includes(news.id));
};